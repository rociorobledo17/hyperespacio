predio del Hospital una Casa Albergue con 10 habitaciones con baños, una sala comedor y sala para manualidades; 
donde se hospedan los niños con uno de sus padres durante el tratamiento intensivo. Hemos reducido de esta 
manera el abandono al tratamiento de un 40% a un 10%. 
En el año 2008 el Ministerio de Salud Pública construye un nuevo servicio de Oncología 
acondicionado por la Asociación Mitaí para la mejor estadía del paciente. 
